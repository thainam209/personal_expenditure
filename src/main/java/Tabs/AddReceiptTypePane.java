    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            ReceiptType enity = new ReceiptType();
            if(txtID.getText().equals("")) lbID.setText("Hãy nhập Id !");
            else {
                lbID.setText("");
                if(txtName.getText().equals("")) lbName.setText("Hãy nhập tên !");
                else {
                    lbName.setText("");
                    enity.setId(txtID.getText());
                    enity.setName(txtName.getText());

                    ReceiptTypeDao dao = new ReceiptTypeDao();
                    enity = dao.insert(enity);

                    if(enity != null){
                    JOptionPane.showMessageDialog(this, "Lưu thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                    lbID.setText("");
                    lbName.setText("");
                    btnUpdate.setEnabled(true);
                    btnDelete.setEnabled(true);
                    btnSave.setEnabled(false);
                    
                    //Lưu xong không cho thay đổi mã
                    txtID.setEnabled(false);
                    }
                    else JOptionPane.showMessageDialog(this, "Lưu thất bại","Thông báo",JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            //e.printStackTrace(); sử dụng để check lỗi tại dòng nào
            JOptionPane.showMessageDialog(this, e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

