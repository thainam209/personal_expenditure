    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            ReceiptType enity = new ReceiptType();
            if(txtID.getText().equals("")) lbID.setText("Hãy nhập Id !");
            else {
                lbID.setText("");
                if(txtName.getText().equals("")) lbName.setText("Hãy nhập tên !");
                else {
                    if(JOptionPane.showConfirmDialog(this, "Bạn có thực sự muốn thay đổi", "",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
                    {
                        lbName.setText("");
                        enity.setId(txtID.getText());
                        enity.setName(txtName.getText());

                        ReceiptTypeDao dao = new ReceiptTypeDao();
                        if(dao.update(enity))
                        {
                            JOptionPane.showMessageDialog(this, "Sửa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                            lbID.setText("");
                            lbName.setText("");
                        }
                        else JOptionPane.showMessageDialog(this, "Sửa thất bại","Lỗi",JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            //e.printStackTrace(); sử dụng để check lỗi tại dòng nào
            JOptionPane.showMessageDialog(this, e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if(JOptionPane.showConfirmDialog(this, "Bạn có thực sự muốn xóa", "",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
            {
                ReceiptTypeDao dao = new ReceiptTypeDao();
                String id = txtID.getText();
                if(dao.delete(id)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                btnAddActionPerformed(evt);
                }
                else JOptionPane.showMessageDialog(this, "Xóa thất bại","Thông báo",JOptionPane.ERROR_MESSAGE);
            }   
            
        } catch (Exception e) {
            //e.printStackTrace(); sử dụng để check lỗi tại dòng nào
            JOptionPane.showMessageDialog(this, e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

