    private void loadTypes(){
        try {
            ReceiptTypeDao dao = new ReceiptTypeDao();
            List<ReceiptType> list = dao.findAll();
            
            for (ReceiptType item : list) {
                cboType.addItem(item);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    //Đẩy dữ liệu lên theo id
    private void loadById(String Id){
        try {
            ReceiptDao dao = new ReceiptDao();
            Receipt entity = dao.findById(Id);
            
            txtID.setText(entity.getID());
            txtName.setText(entity.getName());
            ftfAmount.setText(Double.toString(entity.getAmount()));
            DateUtil du = new DateUtil();
            String date = du.toString(entity.getReceiptDate());
            ftfDate.setText(date);
            txaNote.setText(entity.getNote());
            
            
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
            btnDelete.setEnabled(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }
