    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Receipt entity = new Receipt();
        if(txtName.getText().equals("")) lbName.setText("Hãy nhập tên !");
        else {
            lbName.setText("");
            if(ftfAmount.getText().equals("")) lbAmount.setText("Hãy nhập số lượng !");
            else{
                lbAmount.setText("");
                try{
                    double amount = Double.parseDouble(ftfAmount.getText());
                    lbReceiptDate.setText("");
                    if(ftfDate.getText().equals("")) lbReceiptDate.setText("Hãy nhập ngày chi tiêu !");
                    else{
                        try {
                            if(JOptionPane.showConfirmDialog(this, "Bạn có thực sự muốn thay đổi", "",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
                            {
                                lbReceiptDate.setText("");
                                DateUtil du = new DateUtil();
                                Date ReceiptDate = du.toDate(ftfDate.getText());

                                entity.setID(txtID.getText());
                                entity.setName(txtName.getText());
                                entity.setAmount(amount);

                                ReceiptType ext = (ReceiptType)cboType.getSelectedItem();

                                entity.setIDType(ext.getId());
                                entity.setReceiptDate(ReceiptDate);
                                entity.setNote(txaNote.getText());

                                //lấy tên user từ dialog đăng nhập
                                LoginDialog dialog = new LoginDialog(null, false);
                                String username = dialog.getUsername();

                                entity.setUsername(username);


                                ReceiptDao dao = new ReceiptDao();

                                if(dao.update(entity)){
                                    JOptionPane.showMessageDialog(this, "Chỉnh sửa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                                    lbId.setText("");
                                    lbName.setText("");
                                    lbAmount.setText("");
                                    lbReceiptDate.setText("");                    
                                }
                                else JOptionPane.showMessageDialog(this, "Chỉnh sửa thất bại","Lỗi",JOptionPane.ERROR_MESSAGE);
                            }
                        } catch (Exception e) {
                            lbReceiptDate.setText("Ngày chi tiêu không hợp lệ!");
                        }                  
                }
                }catch(NumberFormatException e){
                    lbAmount.setText("Số lượng không hợp lệ");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if(JOptionPane.showConfirmDialog(this, "Bạn có thực sự muốn xóa", "",JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION)
            {
                ReceiptDao dao = new ReceiptDao();
                String id = txtID.getText();
                if(dao.delete(id)) {
                JOptionPane.showMessageDialog(this, "Xóa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                btnAddActionPerformed(evt);
                }
                else JOptionPane.showMessageDialog(this, "Xóa thất bại","Thông báo",JOptionPane.ERROR_MESSAGE);
            }   
        } catch (Exception e) {
            //e.printStackTrace(); sử dụng để check lỗi tại dòng nào
            JOptionPane.showMessageDialog(this, e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

